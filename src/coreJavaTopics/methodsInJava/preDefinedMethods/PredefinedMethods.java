package coreJavaTopics.methodsInJava.preDefinedMethods;

public class PredefinedMethods {

	public static void main(String[] args) {
		String name = "JohnWick";
		System.out.println("Length of name is: " + name.length());
		
		System.out.println("Maximum number is: " + Math.max(65, 35));
		System.out.println("Minimum number is: " + Math.min(11, 21));
	}

}
